<h2>Raiding Rules</h2>
<p>In addition to the above, any of the following will result in an immediate 12 hour temporary ban if you are attempting to "raid" another player's base, chests or home.</p>
<ul>
    <li>Using /sethome in another town's territory.</li>
    <li>Sleeping in a bed in another town's territory in order to gain access after death.</li>
    <li>Summoning players using /c summon or otherwise.  You must exit claimed land before summoning friends in.</li>
    <li>Breaking a block and using block lag to access a chest.</li>
    <li>Planting trees or any other action that modifies blocks in another town's territory.</li>
    <li>Locking a chest or door or switch in another player's territory.</li>
    <li>CAMPING is illegal.  Camping is defined as any action inhibiting a player's ability to play the game for longer than 10 minutes.</li>
</ul>
