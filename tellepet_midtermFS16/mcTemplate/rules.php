<h2>Immediate Ban!</h2>
<ul>
    <li>Flight, X-Ray, speed or any client-side hack that offers an unfair advantage.</li>
    <li>Major exploitation of bugs in minecraft and/or installed plugins.  If you find one, report to staff directly and immediately.</li>
    <li>Major grief - destroying another player's placed blocks, without permission.</li>
    <li>Extremely sexual or disgusting, racist, homophobic or sexist remarks in chat or otherwise. Keep the language PG-13.</li>
    <li>Persistent harassment, spamming or advertising in any chat channel.</li>
    <li>Breaking through a wall in claimed territory and using block lag to access a chest.</li>
</ul>
<br/>
<h2>Warning! Then Temp Ban if Repeat Offender!</h2>
<ul>
    <li>Chat spamming, including excessive caps, repeating messages or garbage text.</li>
    <li>Excessive use of a foreign language in Global Chat.  The majority of us speak english.  If you're going to speak something else, please keep it to local chat or whispers.</li>
    <li>Asking Staff for Ranks, Op, Admin, nicknames or worldedits.  If we like you, you won't have to ask. </li>
    <li>Asking Staff for free items, munny or experience.  If you lost items due to a glitch, type /helpop, followed by your message while standing at the place that you believe the glitch occurred.</li>
    <li>Asking for a player to be unbanned without posting a ban appeal.  There's a forum for that.  http://x.agoncraft.net/banappealsforum</li>
    <li>Claiming of territory to block a town from building.</li>
    <li>Building garbage structures in unclaimed areas, scrap world excluded.  Clean up after yourself.</li>
    <li>Setting a home in another town's claimed territory.</li>
    <li>Undermining staff. They've been chosen for a reason - If you have a problem with their ruling, post on the forums about it, don't whine and argue in the chat.</li>
    <li>Enderpearling through blocks - if there's a 1x1 hole or half-block (slab, stairs, etc.) you can throw it through, that's fair game, but solid floors are out.</li>
</ul>
